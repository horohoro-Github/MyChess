	static int Read(TArray<int> table, int square, int isWhite)
	{
		if (isWhite)
		{
			int file = square & 0b111;
			int rank = square >> 3;

			rank = 7 - rank;
			square = rank * 8 + file;
		}

		return table[square];
	}



